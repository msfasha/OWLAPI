    public void CaretUpdate(CaretEvent evt) {
        int cursorPosition = evt.getDot();
        CurrentjTextArea = (javax.swing.JTextArea) evt.getSource();
        UserSelectedSegment = "";
        SelectionStartPosition = -1;
        SelectionEndPosition = -1;

        try {
            if (cursorPosition == CurrentjTextArea.getText().length()) {
                return;
            }

            char[] charArray = CurrentjTextArea.getText().toCharArray();

            for (int i = cursorPosition; i >= 0; i--) {
                if (((int) (charArray[cursorPosition - (cursorPosition - i)]) == 32)
                        || (i == 0)) {
                    if (i == 0) {
                        //Just return the current i index
                        SelectionStartPosition = i;
                        break;
                    } else {
                        //if not the first token, then add 1 to compensate for the space after the token
                        //because the begining of this token shall be right after the space character
                        SelectionStartPosition = i + 1;
                        break;
                    }
                }
            }

            for (int i = cursorPosition; i <= charArray.length - 1; i++) {
                if (((int) (charArray[i]) == 32) || (i == charArray.length - 1)) {
                    if (i == charArray.length - 1) {
                        SelectionEndPosition = i;
                        break;
                    } else {
                        SelectionEndPosition = i - 1;
                        break;
                    }
                }
            }

            for (int i = SelectionStartPosition; i <= SelectionEndPosition; i++) {
                UserSelectedSegment = UserSelectedSegment + charArray[i];
            }

            jLabelCurrentSelection.setText(String.valueOf(SelectionStartPosition));
            jLabelStart.setText(String.valueOf(SelectionStartPosition));
            jLabelEnd.setText(String.valueOf(SelectionEndPosition));
            jLabelCurrentSelection.setText(UserSelectedSegment);

            ResetAllToggleButtons(jTabbedPane_Annotation_Tags);
            BuildExistingTaggingFromSelectedString(UserSelectedSegment.trim());
            SelectjTableConceptsListSemanticAnnotationRow(Utility.Globals.StripTextKeepPOSTags(UserSelectedSegment).trim());
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void ResetAllToggleButtons(Container parent) throws Exception {
        for (java.awt.Component c : parent.getComponents()) {
            if (c instanceof javax.swing.JPanel) {
                ResetAllToggleButtons((Container) c);
            }
            if (c instanceof javax.swing.JToggleButton) {
                ((javax.swing.JToggleButton) c).setSelected(false);
            }
        }

        //also clear the table if any rows are selected
        jTableConceptsListSemanticAnnotation.clearSelection();
    }

    private void UpdateAnnotation() {
        //get text prior to selection
        //get text after selection
        //get Arabic token
        //build new annotation
        //build full string accordingly

        try {
            StringBuilder sb = new StringBuilder();

            String preString;
            if (SelectionStartPosition > 0) {
                preString = CurrentjTextArea.getText().substring(0, SelectionStartPosition - 1);//1 to bypass the previous space
                sb.append(preString);
                sb.append(" ");//if this is a pre then there is a later segment so add a space
            }

            String postString = "";
            if (SelectionEndPosition < CurrentjTextArea.getText().length() - 1) {
                //2 so that we bypass the last character of the selected string and bypass the space
                postString = CurrentjTextArea.getText().substring(SelectionEndPosition + 1, CurrentjTextArea.getText().length());
            }

            String arabicToken = Utility.Globals.StripPOSTagsKeepText(UserSelectedSegment);
            String newTagging = BuildNewTokenTaggingFromSelectedToggleButtons();

            if (jTableConceptsListSemanticAnnotation.getSelectedRow() != -1) {//get semantic annotation only if a value is selected in table
                newTagging = newTagging + jTableConceptsListSemanticAnnotation.getValueAt(jTableConceptsListSemanticAnnotation.getSelectedRow(), 0).toString();
            }

            sb.append(arabicToken);

            if (newTagging.length() > 0) {
                sb.append("/");
                sb.append(newTagging);
                SelectionEndPosition = SelectionStartPosition + arabicToken.length() + newTagging.length();
            } else {
                SelectionEndPosition = SelectionStartPosition + arabicToken.length();
            }

            if (postString.length() > 0) {
                if (!(sb.toString().endsWith(" "))) {
                    sb.append(" ");
                }
                sb.append(postString);
            }

            int oldSelectionStartPosition = SelectionStartPosition;
            int oldSelectionEndPosition = SelectionEndPosition;
            CurrentjTextArea.setText(sb.toString());//;/Tweak, replace resulting two spaces with one space
            SelectionStartPosition = oldSelectionStartPosition;
            SelectionEndPosition = oldSelectionEndPosition;

            jLabelEnd.setText(String.valueOf(SelectionEndPosition));
            UserSelectedSegment = CurrentjTextArea.getText().substring(SelectionStartPosition, SelectionEndPosition);
            jLabelCurrentSelection.setText(UserSelectedSegment);

        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(null, ex);
        }
    }

